package com.foodapp.payment.dto;

public class CardPayment {

	public String cardType;
	public String cardHolderName;
	public String cardHolderCardNumber;
	public String cardHolderExpiry;
	public String getCardType() {
		return cardType;
	}
	public void setCardType(String cardType) {
		this.cardType = cardType;
	}
	public String getCardHolderName() {
		return cardHolderName;
	}
	public void setCardHolderName(String cardHolderName) {
		this.cardHolderName = cardHolderName;
	}
	public String getCardHolderCardNumber() {
		return cardHolderCardNumber;
	}
	public void setCardHolderCardNumber(String cardHolderCardNumber) {
		this.cardHolderCardNumber = cardHolderCardNumber;
	}
	public String getCardHolderExpiry() {
		return cardHolderExpiry;
	}
	public void setCardHolderExpiry(String cardHolderExpiry) {
		this.cardHolderExpiry = cardHolderExpiry;
	}
	
	
}
